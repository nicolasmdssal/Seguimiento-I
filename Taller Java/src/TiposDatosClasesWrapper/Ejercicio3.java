package TiposDatosClasesWrapper;

public class Ejercicio3 {
        public static void main(String[] args) {
            String numeroEnTexto = "42";
            int numero = Integer.parseInt(numeroEnTexto);

            String decimalEnTexto = "3.1416";
            double decimal = Double.parseDouble(decimalEnTexto);

            System.out.println("El número entero es: " + numero);
            System.out.println("El número decimal es: " + decimal);
        }
}
